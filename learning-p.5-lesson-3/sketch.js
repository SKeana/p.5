function setup() {
  createCanvas(500, 500); 
}

function draw() {
  background(225, 0, 0); // A light pink background, similar to the video
  fill(0, 0, 225);
  stroke(255, 225, 225);
  strokeWeight(1);
  rect(130, 105, 150, 150, 20);
  stroke(100);
  strokeWeight(2);
  fill(0, 0, 0, 150);
  triangle(230, 275, 258, 220, 286, 275);
}


